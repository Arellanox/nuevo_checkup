<script>
    let traducciones;

    //Hacemos la peticion y la guardamos en la variable traducciones
    $.ajax({
        url: '../../archivos/sistema/json/idioma/en.json',
        type: 'POST',
        dataType: 'json',
        success: function(data) {
            traducciones = data;
        },
        error: function(error) {}
    });

    //Funcion para traducir un texto donde recibe el texto y el idioma
    function traducir(texto, idioma) {
        switch (idioma) {
            case '-en':
                var traduccion = traducciones[texto][idioma];
                if (traduccion == null) {
                    return texto;
                } else {
                    return traduccion;
                }
            default:
                return texto;
        }
    }
</script>